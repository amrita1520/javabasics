public class swithcase {
        public static void main(String[] args) {
            String day = "Monday";
    
            switch (day) {
                case "Monday":
                    System.out.println("Monday");
                    break;
                case "a":
                    System.out.println("Tuesday");
                    break;
                case "b":
                    System.out.println("Wednesday");
                    break;
                default:
                    System.out.println("Invalid Day");
            }
        }
    }

